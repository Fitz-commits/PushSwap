#include "minilib.h"

void	ft_putendl_fd(char *s, int fd)
{
	int i;

	if (s && fd > 0)
	{
		i = 0;
		while (s[i])
		{
			ft_putchar_fd(s[i], fd);
			i++;
		}
		ft_putchar_fd('\n', fd);
	}
}
